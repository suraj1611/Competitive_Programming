ll solve(ll i,ll j)
{
	if(i>j)
		return 0;
	if(i==j)
		return a[i];
	if(i==j-1)
		return abs(a[i]-a[j]);

	ll ans=mn;

	for (int k = i; k <= n; ++k)
	{
		ans=max(ans,abs(solve(i,k)-solve(k+1,n)));
	}

	return ans;
}

